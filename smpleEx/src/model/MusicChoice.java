package model;

public class MusicChoice {
	private String musicNumber;
	private String musicianName;
	private int price;
	private String musicDetail;
	private double afterPrice;

	public String getMusicNumber() {
		return musicNumber;
	}
	public void setMusicNumber(String musicNumber) {
		this.musicNumber = musicNumber;
	}
	public String getMusicianName() {
		return musicianName;
	}
	public void setMusicianName(String musicianName) {
		this.musicianName = musicianName;
	}
	public int getPrice() {
		return price;
	}
	public void setPrice(int price) {
		this.price = price;
	}
	public String getMusicDetail() {
		return musicDetail;
	}
	public void setMusicDetail(String musicDetail) {
		this.musicDetail = musicDetail;
	}

	public double getAfterPrice() {
		return afterPrice;
	}
	public void setAfterPrice(double afterPrice) {
		this.afterPrice = afterPrice;
	}



}
